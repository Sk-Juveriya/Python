#Variables
name="Will"
age=25
marks=90.5

# Numeric Types
integer_num = 10
float_num = 3.14
complex_num = 2 + 3j

# String
text = "Hello Python"

# List (mutable)
fruits = ["apple", "banana", "mango"]

# Tuple (immutable)
points = (10, 20)

# Set (unique values)
numbers = {1, 2, 3, 4}

# Dictionary (key-value pairs)
student = {"name": "John", "age": 21}

# Boolean
is_active = True

# None Type
nothing = None

# Printing values and their types
print("Integer:", integer_num, type(integer_num))
print("Float:", float_num, type(float_num))
print("Complex:", complex_num, type(complex_num))

print("String:", text, type(text))

print("List:", fruits, type(fruits))

print("Tuple:", points, type(points))

print("Set:", numbers, type(numbers))

print("Dictionary:", student, type(student))

print("Boolean:", is_active, type(is_active))

print("None:", nothing, type(nothing))


"""Integer: 10 <class 'int'>
Float: 3.14 <class 'float'>
Complex: (2+3j) <class 'complex'>
String: Hello Python <class 'str'>
List: ['apple', 'banana', 'mango'] <class 'list'>
Tuple: (10, 20) <class 'tuple'>
Set: {1, 2, 3, 4} <class 'set'>
Dictionary: {'name': 'John', 'age': 21} <class 'dict'>
Boolean: True <class 'bool'>
None: None <class 'NoneType'>"""